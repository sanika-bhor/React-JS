class OOPConcept {
  static Addition(num1, num2) {
    this.num1 = num1;
    this.num2 = num2;
    return this.num1 + this.num2;
  }

  static Subtraction(num1, num2) {
    this.num1 = num1;
    this.num2 = num2;
    return this.num1 - this.num2;
  }

  static Multiplication(num1, num2) {
    this.num1 = num1;
    this.num2 = num2;
    return this.num1 * this.num2;
  }

  static Division(num1, num2) {
    this.num1 = num1;
    this.num2 = num2;
    return this.num1 / this.num2;
  }
}
export default OOPConcept;